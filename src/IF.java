public class IF {

    public static void main(String[] args) {
        // if (condition){}
        if (20>18){
            System.out.println("20 IS GREATER THAN 18");
        };

        // Ternary If else (short hand if else)

        String result = (60>30) ? "GoodDay" : "BadDay";


        // Switch

        int marks = 9;
        switch (marks){
            case 9:
                System.out.println("9");
                break;
            case 2:
                System.out.println("e");
        }
    }
}
